// create or update profile
export const createProfile = (formData, history, edit = false) => async dispatch => {
    try {
        const config = {
            headers: {
                'Content-Type': 'application/json'
            }
        }

        const res = await axios.post('/api/profile', formData, config)
        // Show profile after it is created
        dispatch({
            type: GET_PROFILE,
            payload: res.data
        })

        // Show alert that creation/updation is successfull
        dispatch(setAlert(edit ? 'Profile updated successfully' : 'Profile created successfully', 'success'))

        // If profile is created, redirect to dashboard, else no
        if (!edit) {
            history.push('/dashboard')
        }
    } catch (err) {
        const errors = err.response.data.errors
        if (errors) {
            errors.forEach(error => dispatch(setAlert(error.msg, 'danger')))
        }
        dispatch({
            type: PROFILE_ERROR,
            payload: { msg: err.response.statusText, status: err.response.status }
        })
    }
}

